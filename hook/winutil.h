inline void winLog(const std::string& str) {
	OutputDebugStringA((str + "\r\n").c_str());
}

template<class... Args>
void winLog(std::format_string<Args...> format, Args&&... args) {
	winLog(std::format(format, std::forward<Args>(args)...));
}

inline std::string winLastErrorText() {
	DWORD error = GetLastError();
	LPSTR lpMsgBuf = nullptr;
	DWORD bufLen = FormatMessageA(FORMAT_MESSAGE_ALLOCATE_BUFFER |
		FORMAT_MESSAGE_FROM_SYSTEM |
		FORMAT_MESSAGE_IGNORE_INSERTS,
		NULL,
		error,
		MAKELANGID(LANG_NEUTRAL, SUBLANG_DEFAULT),
		(LPSTR)&lpMsgBuf,
		0,
		NULL);
	if (bufLen) {
		std::string result(lpMsgBuf, bufLen);
		LocalFree(lpMsgBuf);
		return result;
	}
	
	return std::string();
}

template<class... Args>
void winLogLastError(std::format_string<Args...> format, Args&&... args) {
	winLog(std::format(format, std::forward<Args>(args)...)
		.append(":")
		.append(winLastErrorText()));
}

